CREATE TABLE IF NOT EXISTS app_users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR(255) NOT NULL UNIQUE,
    password VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS workout (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255),
    date DATE,
    user_id BIGINT NOT NULL
);

CREATE TABLE IF NOT EXISTS exercise (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255),
    workout_id BIGINT NOT NULL
);

CREATE TABLE IF NOT EXISTS exercise_sets (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    reps INTEGER,
    weight DOUBLE PRECISION,
    exercise_id BIGINT NOT NULL
);

DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM pg_constraint WHERE conname = 'fk_workout_user'
    ) THEN
        ALTER TABLE workout
            ADD CONSTRAINT fk_workout_user
            FOREIGN KEY (user_id) REFERENCES app_users (id);
    END IF;
END $$;

DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM pg_constraint WHERE conname = 'fk_exercise_workout'
    ) THEN
        ALTER TABLE exercise
            ADD CONSTRAINT fk_exercise_workout
            FOREIGN KEY (workout_id) REFERENCES workout (id) ON DELETE CASCADE;
    END IF;
END $$;

DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM pg_constraint WHERE conname = 'fk_exercise_sets_exercise'
    ) THEN
        ALTER TABLE exercise_sets
            ADD CONSTRAINT fk_exercise_sets_exercise
            FOREIGN KEY (exercise_id) REFERENCES exercise (id) ON DELETE CASCADE;
    END IF;
END $$;

CREATE INDEX IF NOT EXISTS idx_workout_date ON workout (date);
CREATE INDEX IF NOT EXISTS idx_exercise_workout_id ON exercise (workout_id);
CREATE INDEX IF NOT EXISTS idx_exercise_sets_exercise_id ON exercise_sets (exercise_id);
