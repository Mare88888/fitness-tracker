CREATE TABLE IF NOT EXISTS body_measurement (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    date DATE NOT NULL,
    weight DOUBLE PRECISION,
    waist DOUBLE PRECISION,
    chest DOUBLE PRECISION,
    left_arm DOUBLE PRECISION,
    right_arm DOUBLE PRECISION,
    user_id BIGINT NOT NULL,
    CONSTRAINT fk_body_measurement_user FOREIGN KEY (user_id) REFERENCES app_users (id) ON DELETE CASCADE,
    CONSTRAINT uk_body_measurement_owner_date UNIQUE (user_id, date),
    CONSTRAINT ck_body_measurement_values_positive CHECK (
        (weight IS NULL OR weight > 0)
        AND (waist IS NULL OR waist > 0)
        AND (chest IS NULL OR chest > 0)
        AND (left_arm IS NULL OR left_arm > 0)
        AND (right_arm IS NULL OR right_arm > 0)
    )
);

CREATE INDEX IF NOT EXISTS idx_body_measurement_user_date ON body_measurement (user_id, date);
