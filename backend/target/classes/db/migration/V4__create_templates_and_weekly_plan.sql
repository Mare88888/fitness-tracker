CREATE TABLE IF NOT EXISTS workout_template (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255),
    user_id BIGINT NOT NULL,
    CONSTRAINT fk_template_user FOREIGN KEY (user_id) REFERENCES app_users (id)
);

CREATE TABLE IF NOT EXISTS template_exercise (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255),
    template_id BIGINT NOT NULL,
    CONSTRAINT fk_template_exercise_template FOREIGN KEY (template_id) REFERENCES workout_template (id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS template_sets (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    reps INTEGER,
    weight DOUBLE PRECISION,
    template_exercise_id BIGINT NOT NULL,
    CONSTRAINT fk_template_sets_exercise FOREIGN KEY (template_exercise_id) REFERENCES template_exercise (id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS weekly_plan (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    day_of_week INTEGER NOT NULL,
    user_id BIGINT NOT NULL,
    template_id BIGINT NOT NULL,
    CONSTRAINT fk_weekly_plan_user FOREIGN KEY (user_id) REFERENCES app_users (id),
    CONSTRAINT fk_weekly_plan_template FOREIGN KEY (template_id) REFERENCES workout_template (id),
    CONSTRAINT uk_weekly_plan_owner_day UNIQUE (user_id, day_of_week)
);

CREATE INDEX IF NOT EXISTS idx_workout_template_user ON workout_template (user_id);
CREATE INDEX IF NOT EXISTS idx_template_exercise_template ON template_exercise (template_id);
CREATE INDEX IF NOT EXISTS idx_template_sets_exercise ON template_sets (template_exercise_id);
